public class TiposVariaveis {
    public static void main(String[] args) {
        // Tipo inteiro (int)
        int idade = 25;
        System.out.println("Idade: " + idade);

        // Tipo ponto flutuante (float)
        float altura = 1.75f;
        System.out.println("Altura: " + altura);

        // Tipo ponto flutuante de maior precisão (double)
        double salario = 3500.50;
        System.out.println("Salário: " + salario);

        // Tipo caractere (char)
        char inicialNome = 'A';
        System.out.println("Inicial do nome: " + inicialNome);

        // Tipo booleano (boolean)
        boolean possuiCarro = true;
        System.out.println("Possui carro? " + possuiCarro);

        // Tipo curto (short)
        short anoNascimento = 1998;
        System.out.println("Ano de Nascimento: " + anoNascimento);

        // Tipo longo (long)
        long populacaoMundial = 7800000000L;
        System.out.println("População mundial: " + populacaoMundial);

        // Tipo byte (byte)
        byte idadeCachorro = 10;
        System.out.println("Idade do cachorro: " + idadeCachorro);
    }
}
